//funcoes de operacoes
export function soma(a, b) {
  return a + b;
}

export function subtracao(a, b) {
  return a - b;
}

export function multiplicacao(a, b) {
  return a * b;
}

export function divisao1(a, b) {
  return a / b;
}

export function potenciacao(a, b) {
  return a ** b;
}

export function divisao2(a, b) {
  return a % b;
}
